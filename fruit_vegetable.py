name = input()
user_output = "unknown"

if name == "banana" or name == "apple" or name == "kiwi" or name == "cherry" or name == "lemon" or name == "grapes":
    user_output = "fruit"
elif name == "tomato" or name == "cucumber" or name == "pepper" or name == "carrot":
    user_output = "vegetable"


print(user_output)
